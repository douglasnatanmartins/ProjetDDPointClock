String getDiaSemana(int dia){
  switch(dia){
    case 1:
      return "Lun";
      break;
    case 2:
      return "Mar";
      break;
    case 3:
      return "Mie";
      break;
    case 4:
      return "Jue";
      break;
    case 5:
      return "Vie";
      break;
    case 6:
      return "Sab";
      break;
    case 7:
      return "Dom";
      break;
  }
}